# Import python packages
import streamlit as st
from snowflake.snowpark.functions import col

# Write directly to the app
st.title(f"Customise your smoothie")
st.write(
  """Choose the fruits you want in your custom smoothie
  """
)

cnx = st.connection("snowflake")
session = cnx.session()

my_dataframe = session.table("smoothies.public.fruit_options").select(col('FRUIT_NAME'))
# st.dataframe(data=my_dataframe, use_container_width=True)



order_name = st.text_input("Name on Smoothie ", "")
st.write("The Name on Smoothie will be ", order_name) 



ingredients_list = st.multiselect(
    'Choose upto 5 ingredients:'
    ,my_dataframe
    ,max_selections =5
)
if ingredients_list:
    # st.write(ingredients_list)
    # st.text(ingredients_list)

    ingredients_string = ''
    for fruit_chosen in ingredients_list:
        ingredients_string += fruit_chosen + ' '
        
    st.write(ingredients_string)
    my_insert_stmt = """ insert into smoothies.public.orders(ingredients,name_on_order,order_filled)
            values ('""" + ingredients_string + """','"""+order_name+"""',false)"""


    # st.write(my_insert_stmt)
    insert_button = st.button("Submit Order")
    
    if insert_button:
        session.sql(my_insert_stmt).collect()
        st.success("Your Smoothie is ordered! " + order_name, icon="âœ…")
